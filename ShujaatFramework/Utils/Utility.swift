import Foundation


public class Utility {
    
    public init() {
        
    }
    
    public func getScreenHeight() -> CGFloat {
        return UIScreen.main.bounds.height
    }
    
    public func getScreenWidth() -> CGFloat {
        return UIScreen.main.bounds.width
    }
    
}
